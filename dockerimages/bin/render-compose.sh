#!/usr/bin/env bash
# =====================================================================
# Render compose.yaml from .env + fragment templates.
# Pure bash + envsubst — no Python, no jinja.
# =====================================================================
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
ENV_FILE="${REPO_ROOT}/.env"
TEMPLATES="${REPO_ROOT}/dockerimages/templates"
OUT="${REPO_ROOT}/compose.yaml"

[[ -f "$ENV_FILE" ]] || { echo "✗ .env not found — run 'make init' first" >&2; exit 1; }

# shellcheck disable=SC1090
set -a; . "$ENV_FILE"; set +a

# Default if .env is hand-edited and missing the var
DOCKER_SUBNET_BASE="${DOCKER_SUBNET_BASE:-5}"

# Defensive defaults for the Redis / Varnish image tags. The configurator
# always writes these into .env, but a hand-edited .env from an older
# checkout could be missing them — fall back to the most permissive modern
# versions in that case rather than failing to render.
REDIS_VERSION="${REDIS_VERSION:-7.4}"
VARNISH_VERSION="${VARNISH_VERSION:-7.7}"
export REDIS_VERSION VARNISH_VERSION

# Container IP plan (linux only). On mac we use service-name DNS.
# All IPs are 10.10.<DOCKER_SUBNET_BASE>.<last-octet>; only the last octet
# is service-specific and stays the same across projects so muscle memory
# carries over (.5 is always php-fpm, .10 is always varnish, etc.).
export DOCKER_SUBNET="10.10.${DOCKER_SUBNET_BASE}.0/24"
export DOCKER_GATEWAY="10.10.${DOCKER_SUBNET_BASE}.1"
export DB_IP="10.10.${DOCKER_SUBNET_BASE}.2"
export REDIS_IP="10.10.${DOCKER_SUBNET_BASE}.3"
export WEB_IP="10.10.${DOCKER_SUBNET_BASE}.4"
export PHP_IP="10.10.${DOCKER_SUBNET_BASE}.5"
export OPENSEARCH_IP="10.10.${DOCKER_SUBNET_BASE}.6"
export NODE_IP="10.10.${DOCKER_SUBNET_BASE}.7"
export MAILHOG_IP="10.10.${DOCKER_SUBNET_BASE}.8"
export PMA_IP="10.10.${DOCKER_SUBNET_BASE}.9"
export VARNISH_IP="10.10.${DOCKER_SUBNET_BASE}.10"

# DB image + healthcheck differ between MariaDB and MySQL
if [[ "$DB_ENGINE" == "mariadb" ]]; then
    export DB_IMAGE="mariadb:${DB_VERSION}"
    export DB_VOLUME_MOUNT="./dockerimages/config/database:/etc/mysql/mariadb.conf.d/"
else
    export DB_IMAGE="mysql:${DB_VERSION}"
    export DB_VOLUME_MOUNT="./dockerimages/config/database:/etc/mysql/conf.d/"
fi

# Network block: Linux gets static IPs; Mac/other gets plain network reference.
render_network() {
    local service="$1"; local ip_var="$2"
    if [[ "$HOST_OS" == "linux" ]]; then
        cat <<EOF
    networks:
      ${PROJECT_NAME}-network:
        ipv4_address: ${!ip_var}
EOF
    else
        cat <<EOF
    networks:
      - ${PROJECT_NAME}-network
EOF
    fi
}

# extra_hosts: php-fpm needs to reach SITE_HOST. On Linux point at varnish/web
# static IP so cron, CLI etc. resolve it; on Mac use host-gateway.
render_php_extra_hosts() {
    if [[ "$HOST_OS" == "linux" ]]; then
        local target_ip
        if [[ "$USE_VARNISH" == "yes" ]]; then target_ip="$VARNISH_IP"; else target_ip="$WEB_IP"; fi
        cat <<EOF
    extra_hosts:
      - "${SITE_HOST}:${target_ip}"
EOF
    else
        cat <<EOF
    extra_hosts:
      - "host.docker.internal:host-gateway"
      - "${SITE_HOST}:host-gateway"
EOF
    fi
}

# xdebug client_host: on Linux point to host-gateway via extra_hosts; on Mac
# use the magic host.docker.internal that Docker Desktop provides natively.
if [[ "$HOST_OS" == "linux" ]]; then
    export XDEBUG_CLIENT_HOST="host.docker.internal"
else
    export XDEBUG_CLIENT_HOST="host.docker.internal"
fi

# ---------------------------------------------------------------------
# Begin assembly
# ---------------------------------------------------------------------
{
cat <<EOF
# =====================================================================
# Generated by dockerimages/bin/render-compose.sh — do not edit by hand.
# Re-run \`make rebuild\` after changing .env to regenerate this file.
#
# Project   : ${PROJECT_NAME}
# Platform  : ${FLAVOUR} ${MAGENTO_VERSION}
# PHP       : ${PHP_VERSION}
# Database  : ${DB_ENGINE}:${DB_VERSION}
# Varnish   : ${USE_VARNISH}    Node.js: ${USE_NODE}
# Host OS   : ${HOST_OS}        Static IPs: $([[ "$HOST_OS" == linux ]] && echo "yes (${DOCKER_SUBNET})" || echo no)
# =====================================================================
services:
  db:
    image: ${DB_IMAGE}
    shm_size: 2gb
    ports:
      - "3300:3306"
    volumes:
      - '${PROJECT_NAME}-db:/var/lib/mysql'
      - '${DB_VOLUME_MOUNT}'
    environment:
      - MYSQL_ROOT_PASSWORD=\${MYSQL_ROOT_PASSWORD}
      - MYSQL_DATABASE=\${MYSQL_DATABASE}
      - MYSQL_USER=\${MYSQL_USER}
      - MYSQL_PASSWORD=\${MYSQL_PASSWORD}
EOF
render_network db DB_IP

cat <<EOF

  # Redis tag is auto-selected from REDIS_VERSION in .env, which the
  # configurator pulls from the per-Magento-version compat matrix
  # (e.g. 2.4.6 → 7.0, 2.4.7 → 7.2, 2.4.8/2.4.9 → 7.4 per Adobe).
  # MageOS allows Redis 7.0+ or Valkey — swap the image line to
  # valkey/valkey:8-alpine if you want Valkey instead.
  redis:
    image: 'redis:${REDIS_VERSION}-alpine'
    ports:
      - "6379"
    sysctls:
      net.core.somaxconn: 1024
    ulimits:
      nproc: 65535
      nofile:
        soft: 20000
        hard: 40000
    healthcheck:
      test: 'redis-cli ping || exit 1'
      interval: 30s
      timeout: 30s
      retries: 3
EOF
render_network redis REDIS_IP

cat <<EOF

  web:
    build:
      context: ./dockerimages/config/nginx
      args:
        SITE_HOST: "${SITE_HOST}"
        SITE_NAME: "${SITE_NAME}"
    volumes:
      - ./httpdocs:/var/www/html
      - ./dockerimages/config/nginx/nginx.conf:/etc/nginx/nginx.conf
      - ./dockerimages/config/nginx/sites/:/etc/nginx/sites-available
      - ./dockerimages/config/nginx/conf.d/:/etc/nginx/conf.d
    depends_on:
      - php-fpm
    ports:
      - "80:80"
      - "443:443"
    environment:
      SITE_HOST: "${SITE_HOST}"
      USE_VARNISH: "${USE_VARNISH}"
EOF
render_network web WEB_IP

cat <<EOF

  php-fpm:
    build:
      context: ./dockerimages/config/php-fpm
      args:
        PHP_VERSION: "${PHP_VERSION}"
    volumes:
      - ./httpdocs:/var/www/html
      - ./dockerimages/config/php-fpm/config/php-fpm.conf:/usr/local/etc/php-fpm.conf
      - ./dockerimages/config/php-fpm/config/php-fpm.ini:/usr/local/etc/php/conf.d/zzz-php-config.ini
      - ./dockerimages/config/php-fpm/config/xdebug.ini:/usr/local/etc/php/conf.d/zzz-xdebug-config.ini
      - ./dockerimages/config/php-fpm/users/.bash_history:/var/www/.bash_history
      - ./dockerimages/config/php-fpm/users/.bashrc:/var/www/.bashrc
      - ./db_dumps:/var/www/db_dumps
      - magento-composer-cache:/var/www/.composer
    depends_on:
      - db
      - redis
      - opensearch
      - mailhog
    ports:
      - "9000"
      - "9001"
      - "9003"
    environment:
      XDEBUG_CONFIG: "client_host=${XDEBUG_CLIENT_HOST}"
      PHP_IDE_CONFIG: "serverName=${SITE_NAME}Docker"
      COMPOSER_MEMORY_LIMIT: 2G
      SITE_HOST: "${SITE_HOST}"
      MYSQL_DATABASE: "\${MYSQL_DATABASE}"
      MYSQL_USER: "\${MYSQL_USER}"
      MYSQL_PASSWORD: "\${MYSQL_PASSWORD}"
      ADMIN_EMAIL: "\${ADMIN_EMAIL}"
EOF
render_php_extra_hosts
render_network php-fpm PHP_IP

cat <<EOF

  opensearch:
    image: opensearchproject/opensearch:${OPENSEARCH_VERSION}
    # OpenSearch's clean-shutdown takes ~30s by default. For a dev stack we
    # don't care about index integrity — kill it after 5s.
    stop_grace_period: 5s
    ulimits:
      memlock:
        soft: -1
        hard: -1
    environment:
      - cluster.name=docker-cluster
      - discovery.type=single-node
      - bootstrap.memory_lock=true
      # Disable everything security-related — this is a dev container, no
      # SSL certs, no admin password, no demo config installed.
      - DISABLE_SECURITY_PLUGIN=true
      - DISABLE_INSTALL_DEMO_CONFIG=true
      # Plugins required by Magento for unicode tokenisation + phonetic
      # search. The official image installs them on first start when this
      # env var is set.
      - OPENSEARCH_PLUGINS=analysis-icu,analysis-phonetic
      - OPENSEARCH_JAVA_OPTS=-Xms512m -Xmx512m
EOF
render_network opensearch OPENSEARCH_IP

cat <<EOF

  mailhog:
    image: mailhog/mailhog:latest
    ports:
      - "1025:1025"
      - "8025:8025"
EOF
render_network mailhog MAILHOG_IP

cat <<EOF

  phpmyadmin:
    image: phpmyadmin/phpmyadmin
    environment:
      - PMA_HOST=db
      - PMA_USER=root
      - PMA_PASSWORD=\${MYSQL_ROOT_PASSWORD}
      - MYSQL_ROOT_PASSWORD=\${MYSQL_ROOT_PASSWORD}
      - UPLOAD_LIMIT=300M
    ports:
      - "8080:80"
    depends_on:
      - db
EOF
render_network phpmyadmin PMA_IP

# Optional: Node.js
if [[ "$USE_NODE" == "yes" ]]; then
cat <<EOF

  nodejs:
    build:
      context: ./dockerimages/config/nodejs
      args:
        USER_UID: 1000
        USER_GID: 1000
        SITE_HOST: "${SITE_HOST}"
        SITE_NAME: "${SITE_NAME}"
    stdin_open: true
    tty: true
    volumes:
      - ./httpdocs:/var/www/html
      - ./dockerimages/config/nodejs/passwd:/etc/passwd
    ports:
      - "5173:5173"
EOF
render_network nodejs NODE_IP
fi

# Optional: Varnish
if [[ "$USE_VARNISH" == "yes" ]]; then
cat <<EOF

  varnish:
    image: varnish:${VARNISH_VERSION}
    depends_on:
      - php-fpm
    ports:
      - "8081:80"
    volumes:
      - ./dockerimages/config/varnish/default.vcl:/etc/varnish/default.vcl
    command: "-p default_keep=36000 -p default_ttl=36000 -p default_grace=36000 -f /etc/varnish/default.vcl -s malloc,1G"
EOF
render_network varnish VARNISH_IP
fi

# Networks + volumes footer
cat <<EOF

networks:
  ${PROJECT_NAME}-network:
    driver: bridge
EOF

if [[ "$HOST_OS" == "linux" ]]; then
cat <<EOF
    ipam:
      driver: default
      config:
        - subnet: ${DOCKER_SUBNET}
          gateway: ${DOCKER_GATEWAY}
EOF
else
cat <<EOF
    ipam:
      driver: default
EOF
fi

cat <<EOF

volumes:
  ${PROJECT_NAME}-db: { }
  magento-composer-cache:
      external: true
      name: magento-composer-cache
EOF

} > "$OUT"

echo "✓ rendered $OUT"
